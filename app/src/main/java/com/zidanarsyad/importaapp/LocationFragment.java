import com.google.android.gms.tasks.OnSuccessListener;
import com.google.android.gms.tasks.Task;

public class LocationFragment extends Fragment {
    View view;
    TextView textLatitude, textLongitude;
    FusedLocationProviderClient fusedLocationClient;

    public LocationFragment() {
        // Required empty public constructor
    }

    @SuppressLint("MissingPermission")
    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        view = inflater.inflate(R.layout.fragment_location, container, false);

        textLatitude = (TextView) view.findViewById(R.id.textview_latitude);
        textLongitude = (TextView) view.findViewById(R.id.textview_longitude);

        fusedLocationClient = LocationServices.getFusedLocationProviderClient(getActivity());
        fusedLocationClient.getLastLocation()
                .addOnSuccessListener(getActivity(), new OnSuccessListener<Location>() {
                    @Override
                    public void onSuccess(Location location) {
                        // Got last known location. In some rare situations this can be null.
                        if (location != null) {
                            // Logic to handle location object
                            textLatitude.setText(String.valueOf(location.getLatitude()));
                            textLongitude.setText(String.valueOf(location.getLongitude()));
                        }
                    }
                });
        return view;
    }
}